namespace NES
{

///////////////////////////////////////////////////////////////////////////////
IRQHandler::IRQHandler(int bit, CPU& cpu)
    : bit(bit), cpu(cpu)
{}

///////////////////////////////////////////////////////////////////////////////
void IRQHandler::Release(void)
{
    cpu.SetIRQPulldown(bit, false);
}

///////////////////////////////////////////////////////////////////////////////
void IRQHandler::Pull(void)
{
    cpu.SetIRQPulldown(bit, true);
}

} // !namespace NES
