namespace NES::Audio
{

///////////////////////////////////////////////////////////////////////////////
/// \brief
///
///////////////////////////////////////////////////////////////////////////////
class Noise
{
public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    enum class Mode : bool
    {
        Bit1 = 0,
        Bit6 = 1
    };

public:
    ///////////////////////////////////////////////////////////////////////////
    // Public members
    ///////////////////////////////////////////////////////////////////////////
    Volume volume;          //< Volume of the noise channel
    LengthCounter counter;  //< Length counter for the noise channel
    Divider divider;        //< Divider for the noise channel
    Mode mode;              //< Mode of the noise channel (Bit1 or Bit6)
    int period;             //< Period of the noise channel
    int shiftRegister;      //< Shift register for the noise channel

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    Noise(void);

public:
    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \param index
    ///
    ///////////////////////////////////////////////////////////////////////////
    void SetPeriodFromTable(int index);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    ///////////////////////////////////////////////////////////////////////////
    void Clock(void);

    ///////////////////////////////////////////////////////////////////////////
    /// \brief
    ///
    /// \return
    ///
    ///////////////////////////////////////////////////////////////////////////
    Byte Sample(void) const;
};

} // !namespace NES::Audio
